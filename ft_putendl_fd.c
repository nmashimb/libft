#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	char nl;

	if (!s)
		return ;
	while (*s)
	{
		write(fd, s, 1);
		s++;
	}
	nl = '\n';
	write(fd, &nl, 1);
}
