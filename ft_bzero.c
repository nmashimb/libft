#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*hold_str;

	i = 0;
	hold_str = (char *)s;
	while (i < n)
	{
		hold_str[i] = 0;
		i++;
	}
}
