char	*ft_strncpy(char *des, char *src, int n)
{
	int i;

	i = 0;
	while (src[i] != '\0' && i < n)
	{
		des[i] = src[i];
		i++;
	}
	while (i < n)
	{
		des[i] = '\0';
		i++;
	}
	return (des);
}
