int		ft_atoi(const char *str)
{
	int sign;
	int i;
	int res;

	i = 0;
	sign = 1;
	while (str[i] != '\0' && (str[i] == '\t' || str[i] == ' '))
		i++;
	if (str[i] == '-')
	{
		sign = -1;
		i++;
	}
	res = 0;
	while (str[i] != '\0' && (str[i] >= 48 && str[i] <= 57))
	{
		res = res * 10 + str[i] - 48;
		i++;
	}
	return (sign * res);
}
